#include "libftprintf.h"

void	ft_putnbr(int n)
{
	char	c;

	if (n == INT_MIN)
	{
		write (1, "-2", 2);
		n = 147483648;
	}
	if (n < 0)
	{
		write (1, "-", 1);
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr((n / 10));
		n = n % 10;
	}
	if (n <= 9 && n >= 0)
	{
		c = (48 + n);
		write (1, &c, 1);
	}
}
